void drawtab()
{
    for(int i=0; i<60; i++)
        {
         for(int j=0;j<60;j++)
         {
             if(matrizobs[i][j]==1)
             {
                 gotoxy(j+1,i+1);
                 cout<<"*";
             }

         }
        }


}

void drawSnake()
{

    for (int i=0;i<snakeSize;i++)
    {
        textbackground(GREEN);
        textcolor(YELLOW);
        if(i==0)
        {
            textbackground(RED);
        }
        gotoxy(snakeX[i],snakeY[i]);
        cprintf("@");
    }
}
void colicion()
{
    if(matrizobs[snakeY[0]-1][snakeX[0]-1]==1)
        {
            alive=false;
        }
}
void moveSnake()
{
    gotoxy(snakeX[snakeSize-1],snakeY[snakeSize-1]);
    cout<<" ";

    for(int i=snakeSize-1;i>0;i--)
    {
        snakeX[i]=snakeX[i-1];
        snakeY[i]=snakeY[i-1];
    }

    switch(direccion)
    {
        case 1:
            snakeY[0]--;
        break;
        case 2:
            snakeY[0]++;
        break;
        case 3:
            snakeX[0]--;
        break;
        case 4:
            snakeX[0]++;
        break;
    }
    colicion();
}

